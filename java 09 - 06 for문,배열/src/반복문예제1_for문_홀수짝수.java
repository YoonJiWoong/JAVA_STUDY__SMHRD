
public class 반복문예제1_for문_홀수짝수 {

	public static void main(String[] args) {
		//for문을 활용해서 21~57까지 숫자 중 홀수만 출력하기
		//출력결과가 가로로 나오기
		for(int i =21; i<=57; i+=2) {// 2씩 더하기 반복문
		System.out.print(i+" ");
		}
		System.out.println();
		for(int i =96; i>=53; i--) {
		System.out.print(i+ " ");
		}
		}
			
	}


